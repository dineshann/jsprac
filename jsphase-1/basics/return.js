/** JavaScript Return Type **/
/*
    JavaScript, the return keyword is used within a function to specify the value 
    that the function should produce as its result or return to the caller. When a 
    function is executed and encounters a return statement, it immediately stops 
    executing and returns the specified value to the calling code. 

    Here's how the return keyword works:
*/
function myname()
{
    return "I'm Back Dinesh"
}

// myname()
var a = myname()
console.log(a)

